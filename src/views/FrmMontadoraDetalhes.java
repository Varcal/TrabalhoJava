    private void chamarFormMontadoraCadastro(AcaoNaTela acao, Montadora montadora) {
        FrmMontadoraCadastro frm = new FrmMontadoraCadastro(_internalPrincipal,acao, montadora);
        this.dispose();
        _internalPrincipal.add(frm);
        frm.setVisible(true);
    }
    
    private void carregarTabela(String situacao, String pesquisa){
        
        ArrayList<Montadora> montadoras = new ArrayList<Montadora>();
        
        try{
            montadoras = _controller.selecionarTodosAtivosInativos(situacao ,pesquisa);
                     
            if(montadoras == null){
                JOptionPane.showMessageDialog(null, "Nenhuma montadora cadastrada", "INFORMAÇÕES", JOptionPane.INFORMATION_MESSAGE);
            }
            
            DefaultTableModel table = (DefaultTableModel) gridMontadoras.getModel();
            
            for(Montadora m: montadoras){
                table.addRow(new Object[]{m.getId(), m.getNome(),m.isAtivo()? "Ativo" : "Inativo"});
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Não foi possível carregas as informações Detalhes: "+ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    private void removerLinha(int linhaSelecionada){
        DefaultTableModel table = (DefaultTableModel) gridMontadoras.getModel();
        
        table.removeRow(linhaSelecionada);
    }
        
    private Montadora retornaObjetoLinhaSelecionada(int linhaSelecionada){
        
        int id =  (Integer)gridMontadoras.getValueAt(linhaSelecionada, 0);
        String nome = (String)gridMontadoras.getValueAt(linhaSelecionada, 1);
        boolean ativo = (String)gridMontadoras.getValueAt(linhaSelecionada, 2) == "Ativo";                          
        Montadora montadora = new Montadora(id, nome, ativo);    
        
        return montadora;
    }
