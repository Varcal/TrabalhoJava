    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        
        int linhaSelecionada  = gridMontadoras.getSelectedRow();
        
        if(linhaSelecionada <= 0){
            JOptionPane.showMessageDialog(null, "Nenhuma montadora foi selecionda.", "INFORMAÇÔES", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        Montadora montadora = retornaObjetoLinhaSelecionada(linhaSelecionada);
      
        chamarFormMontadoraCadastro(AcaoNaTela.Editar, montadora);
    }//GEN-LAST:event_btnAlterarActionPerformed

